def deal_card():
    cards_set = [11, 2, 3, 4, 5, 6, 7, 8, 9, 10, 10, 10, 10]
    card = random.choice(cards_set)
    return card


def calc_score(player_cards):
    if sum(player_cards) == 21 and len(player_cards) == 2:
        return 0
    if sum(player_cards) > 21 and 11 in player_cards:
        player_cards.remove(11)
        player_cards.append(1)
    return sum(player_cards)


def compare(user_score, computer_score):
    # Bug fix. If you and the computer are both over, you lose.
    if user_score > 21 and computer_score > 21:
        return "You went over. You lose ðŸ˜¤"

    if user_score == computer_score:
        return "Draw ðŸ™ƒ"
    elif computer_score == 0:
        return "Lose, opponent has Blackjack ðŸ˜±"
    elif user_score == 0:
        return "Win with a Blackjack ðŸ˜Ž"
    elif user_score > 21:
        return "You went over. You lose ðŸ˜­"
    elif computer_score > 21:
        return "Opponent went over. You win ðŸ˜"
    elif user_score > computer_score:
        return "You win ðŸ˜ƒ"
    else:
        return "You lose ðŸ˜¤"


def play_game():
    user_cards = []
    computer_cards = []
    is_game_over = False

    for i in range(2):
        user_cards.append(deal_card())
        computer_cards.append(deal_card())

    while not is_game_over:
        user_score = calc_score(user_cards)
        comp_score = calc_score(computer_cards)
        print(f"   Your cards: {user_cards}, current score: {user_score}")
        print(f"   Computer's first card: {computer_cards[0]}")

        if user_score == 0 or comp_score == 0 or user_score > 21:
            is_game_over = True

        else:
            # Hint 10: If the game has not ended, ask the user if they want to draw another card.
            # If yes, then use the deal_card() function to add another card to the user_cards List.
            # If no, then the game has ended.
            user_should_deal = input("Type 'y' to get another card, type 'n' to pass: ")
            if user_should_deal == "y":
                user_cards.append(deal_card())
            else:
                is_game_over = True

    # Hint 12: Once the user is done, it's time to let the computer play. The computer should keep drawing cards as long as it has a score less than 17.
    while comp_score != 0 and comp_score < 17:
        computer_cards.append(deal_card())
        comp_score = calc_score(computer_cards)

    print(f"   Your final hand: {user_cards}, final score: {user_score}")
    print(f"   Computer's final hand: {computer_cards}, final score: {comp_score}")
    print(compare(user_score, comp_score))

while input("Do you want to play a game of Blackjack? Type 'y' or 'n': ") == "y":
    play_game()
